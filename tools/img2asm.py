def arr2dec(arr):
    num = 0
    for i in range(len(arr)):
        num += arr[i]*2**i
    return num


def img2hackC(imgs):
    imgs[:, 0, :] = 1
    imgs[:, :, 0] = 1
    with open('vardef.txt', 'w') as o:
        values = []
        for idx in range(len(imgs)):
            for row in range(len(imgs[idx])):
                c = f'var l_{idx}_{row} = {arr2dec(imgs[idx][row])};'
                values.append(c)
                o.write(c+'\n')


def imgDisplayer(imgs, sc='screenPtr'):
    li = len(imgs)
    prescaler = 1000//li

    cs = img2hackC(imgs)
    r = ''
    r += f'var cp = {sc};\n'
    for i in range(li):
        r += f'if(({i*prescaler}<=frame)&(frame<{(i+1)*prescaler}))'+'{\n'
        for j in range(len(imgs[0])):
            r += f'*cp = l_{i}_{j};\n'
            r += f'cp = cp + 32;\n'
        r += '}\n'
    r += 'frame++;\n'
    r += f'if(frame=={li*prescaler})frame=0;\n'
    return r


with open('source.txt', 'w') as f:
    f.write(imgDisplayer(default))

